$(document).ready(function () {
//Index
    $(".delete-event").submit(function (event) {
        event.preventDefault();
        if (confirm("Are you sure you want to delete this event?")) {
            $.ajax({
                type: "POST",
                dataType: "html",
                data: $(this).serialize(),
                url: $("#DeleteEventUrl").val(),
                success: function (result) {
                    location.reload();
                }
            });
        }
    });
//Detail
    $("#edit-event").hide();
    $("#edit-form-btn").click(function () {
        $("#edit-event").toggle();
    });
    $("#edit-event").submit(function (event) {
        event.preventDefault();
        $.ajax({
            type: "POST",
            dataType: "html",
            data: $(this).serialize(),
            url: $("#EditEventUrl").val(),
            success: function (result) {
                location.reload();
            }
        });
    });
    $(".delete-event-details").submit(function (event) {
        event.preventDefault();
        if (confirm("Are you sure you want to delete this event?")) {
            $.ajax({
                type: "POST",
                dataType: "html",
                data: $(this).serialize(),
                url: $("#DeleteCurrentEventUrl").val(),
                success: function (result) {
                    location.replace("Index");
                }
            });
        }
    });
//User Events Map
    $("#map-user-events").submit(function (event) {
        event.preventDefault();
        $.ajax({
            type: "POST",
            dataType: "json",
            data: $(this).serialize(),
            url: $("#UserEventsMapUrl").val(),
            success: function (eventsList) {
                var userLocationImg = "http://maps.google.com/mapfiles/ms/icons/purple-dot.png";
                var showMapDiv = document.getElementById("show-events-map");

                var geoSuccess = function (position) {
                    //position is autogenerated by HTML Geolocation
                    var userLat = position.coords.latitude;
                    var userLng = position.coords.longitude;

                    var defaultMapOptions = {
                        center: { lat: userLat, lng: userLng }, //Center map on user location
                        zoom: 16,
                    }
                    var map = new google.maps.Map(showMapDiv, defaultMapOptions);

                    //Set marker for user location
                    var userLocationMarker = new google.maps.Marker({
                        map: map,
                        position: { lat: userLat, lng: userLng },
                        icon: userLocationImg
                    });
                    var userInfoWindow = new google.maps.InfoWindow({
                        content: "Your Location"
                    });
                    userLocationMarker.addListener("click", function () {
                        userInfoWindow.open(map, userLocationMarker);
                    });

                    //Pass mapEvent() function through loop with event object so event properties are accessibly throughout entire function
                    for (var i = 0; i < eventsList.length; i++)
                    {
                        mapEvent(map, eventsList[i]);
                    }
                    
                }
                    
                //Set markers for all events
                var mapEvent = function (map, event) {
                    var geocoder = new google.maps.Geocoder();

                    var infoWindowContent = "<a href='/Event/Detail?eventId=" + event.id + "'>" + event.description + "</a>";

                    geocoder.geocode({ "address": event.address }, function (results, status) {
                        if (status == "OK") {
                            var eventMarker = new google.maps.Marker({
                                map: map,
                                position: results[0].geometry.location
                            });
                            var eventInfoWindow = new google.maps.InfoWindow({
                                content: infoWindowContent
                            });
                            eventMarker.addListener("click", function () {
                                eventInfoWindow.open(map, eventMarker);
                            });
                        } else {
                            alert("Geocode was not successful for the following reason: " + status);
                        }
                    });
                }

                var geoError = function (error) {
                    console.log("Error occurred. Error code: " + error.code);
                }

                var geoOptions = {
                    enableHighAccuracy: true
                }

                navigator.geolocation.getCurrentPosition(geoSuccess, geoError, geoOptions);
            }
        });
    });
});